def get_category_config(category_key):
    """
    获取类目配置
    
    Args:
        category_key: 类目键（如 'bicycle'）
    
    Returns:
        dict: 类目配置
    """
    return CATEGORY_CONFIGS.get(category_key, CATEGORY_CONFIGS['bicycle'])


def get_all_categories():
    """
    获取所有类目列表
    
    Returns:
        list: 类目键列表
    """
    return list(CATEGORY_CONFIGS.keys())


def get_category_name(category_key):
    """
    获取类目中文名称
    
    Args:
        category_key: 类目键
    
    Returns:
        str: 类目中文名称
    """
    config = get_category_config(category_key)
    return config.get('name', '未知类目')
